    private void btnSendOTPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendOTPActionPerformed
        try{
            Sender otp=new OTPSender();
            String number=txtMoileNo.getText().trim();
            String data=String.valueOf(ThreadLocalRandom.current().nextInt(100000,999999));
            int otpNo=Integer.parseInt(data);
            patient.setOtp(otpNo);
            boolean res=otp.send(number,data);
            if(res){
                JOptionPane.showMessageDialog(null, "OTP Sended Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "OTP NOT Sended ", "Error", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSendOTPActionPerformed

