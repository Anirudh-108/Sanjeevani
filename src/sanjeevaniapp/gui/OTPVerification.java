    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
        if (!isValidOTP()) {
            JOptionPane.showMessageDialog(null, "Invalid OTP, Try Again", "Invalid OTP", JOptionPane.ERROR_MESSAGE);
            txtOTP.setText("");
            return;
        }
        try {
            AppointmentPojo appopojo = new AppointmentPojo();
            String date=patient.getDate().toString();
            appopojo.setApptDate(date);
            appopojo.setMoileNo(patient.getMno());
            appopojo.setOpd(patient.getOpd());
            appopojo.setPatientId(patient.getPatientId());
            appopojo.setPatientName(txtPatientName.getText().trim());
            appopojo.setStatus("REQUEST");
            String docName = DoctorDao.getDoctorNameById(patient.getDoctorId());
            appopojo.setDoctorName(docName);
            boolean res1 = PatientDao.addPatient(patient);
            boolean res2 = AppointmentDao.addAppointment(appopojo);
            if (res1 && res2) {
                JOptionPane.showMessageDialog(null, "Patient Record Saved", "Success", JOptionPane.INFORMATION_MESSAGE);
                ViewAllPatients frame = new ViewAllPatients();
                this.dispose();
                frame.setVisible(true);
            }else
            {
                JOptionPane.showMessageDialog(null, "Sorry!, Could not Saved Patient Record", "Failure", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnVerifyActionPerformed

